public class DataTypes {
    public static void main(String[] args) {
        // byte Data type 8 bits
        byte byte1 = 127 ;

        System.out.println(byte1);
//        Short data type 16 bits
        short short1 = 1200;
        System.out.println(short1);

        // int Data type 32 bits
        int range = 12;

        System.out.println(range);
        System.out.println(range+1);
        // Long Data type 64 bits
        long long1 = 12 ;

        System.out.println(range);
        System.out.println(range+1);

        // float data type 32bits

        float float1 = 12;
        System.out.println(float1);

        // double data type 64 bits

        double double1 = 12;
        System.out.println(double1);

        // Character Data type 16 bits
        char char1 = 'A';
        System.out.println((int)char1);

//        boolean data type 1 bit
        boolean boolean1 = false;

    }
}
